#include "zf_device_imu660ra.h"

int16 imu660ra_gyro_x = 0, imu660ra_gyro_y = 0, imu660ra_gyro_z = 0;            // 三轴陀螺仪数据   gyro (陀螺仪)
int16 imu660ra_acc_x = 0, imu660ra_acc_y = 0, imu660ra_acc_z = 0;               // 三轴加速度计数据 acc  (accelerometer 加速度计)

#define GET_IMU660RA_SDA                gpio_read_pin(IMU660RA_SDA_PIN)         // 获取 SDA 电平
#define IMU660RA_SCL_LOW()              gpio_clear_pin(IMU660RA_SCL_PIN)        // SCL 输出低电平
#define IMU660RA_SCL_HIGH()             gpio_set_pin(IMU660RA_SCL_PIN)          // SCL 输出高电平
#define IMU660RA_SDA_LOW()              gpio_clear_pin(IMU660RA_SDA_PIN)        // SDA 输出低电平
#define IMU660RA_SDA_HIGH()             gpio_set_pin(IMU660RA_SDA_PIN)          // SDA 输出高电平


#define ack     1                                                               // 主应答
#define no_ack  0                                                               // 从应答

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 软延时
// 参数说明     void            无参数
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_delay (void)
{
    vuint16 j = IMU660RA_SOFT_IIC_DELAY;
    while(j --);
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 起始信号
// 参数说明     void            无参数
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_start(void)
{
    IMU660RA_SDA_HIGH();                                                        // 拉高总线
    IMU660RA_SCL_HIGH();                                                        // 拉高总线
    imu660ra_simiic_delay();
    IMU660RA_SDA_LOW();                                                         // 先拉低 SDA
    imu660ra_simiic_delay();
    IMU660RA_SCL_LOW();                                                         // 再拉低 SCL
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 停止信号
// 参数说明     void            无参数
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_stop(void)
{
    IMU660RA_SDA_LOW();                                                         // 拉低总线
    IMU660RA_SCL_LOW();                                                         // 拉低总线
    imu660ra_simiic_delay();
    IMU660RA_SCL_HIGH();                                                        // 先拉高 SCL
    imu660ra_simiic_delay();
    IMU660RA_SDA_HIGH();                                                        // 再拉高 SDA
    imu660ra_simiic_delay();
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 主机应答信号
// 参数说明     ack_dat         是否产生 ACK 信号 0-NACK 1-ACK
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_sendack (unsigned char ack_dat)
{
    IMU660RA_SCL_LOW();                                                         // SCL 时钟线拉低
    imu660ra_simiic_delay();
    if(ack_dat)                                                                 // 判断是否输出 ACK
    {
        IMU660RA_SDA_LOW();                                                     // ACK 则拉低 SDA
    }
    else
    {
        IMU660RA_SDA_HIGH();                                                    // NACK 则拉高 SDA
    }

    IMU660RA_SCL_HIGH();                                                        // SCL 时钟线拉高
    imu660ra_simiic_delay();
    IMU660RA_SCL_LOW();                                                         // SCL 时钟线拉低 完成 ACK 时钟周期
    imu660ra_simiic_delay();
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 等待应答信号
// 参数说明     void            无参数
// 返回参数     int             0-ACK 1-NACK
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static int imu660ra_sccb_waitack (void)
{
    int return_state = 1;
    IMU660RA_SCL_LOW();                                                         // SCL 时钟线拉低

    imu660ra_simiic_delay();

    IMU660RA_SCL_HIGH();                                                        // SCL 时钟线拉高 进入 ACK 时钟周期
    imu660ra_simiic_delay();

    if(GET_IMU660RA_SDA)                                                        // 判断此时 SDA 信号 低电平则正常应答 否则异常
    {
        return_state = 0;
    }
    IMU660RA_SCL_LOW();                                                         // SCL 时钟线拉低 结束退出
    imu660ra_simiic_delay();
    return return_state;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 发送字节
// 参数说明     c               发送的数据
// 返回参数     void            无返回数据
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_send_ch (uint8 c)
{
    uint8 i = 8;
    while(i --)
    {
        if(c & 0x80)
        {
            IMU660RA_SDA_HIGH();                                                // SDA 输出数据
        }
        else
        {
            IMU660RA_SDA_LOW();
        }
        c <<= 1;
        imu660ra_simiic_delay();
        IMU660RA_SCL_HIGH();                                                    // SCL 时钟线拉高 数据维持稳定 从机采集数据
        imu660ra_simiic_delay();
        IMU660RA_SCL_LOW();                                                     // SCL 时钟线拉低
    }
    imu660ra_sccb_waitack();                                                    // 等待从机应答
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 读取字节
// 参数说明     ack_x           是否产生 ACK 信号 0-NACK 1-ACK
// 返回参数     uint8           读取的数据
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static uint8 imu660ra_read_ch (uint8 ack_x)
{
    uint8 i;
    uint8 c = 0;

    IMU660RA_SCL_LOW();                                                         // SCL 拉低总线占用
    imu660ra_simiic_delay();
    IMU660RA_SDA_HIGH();                                                        // SDA 拉高释放数据线

    for(i = 0; 8 > i; i ++)
    {
        imu660ra_simiic_delay();
        IMU660RA_SCL_LOW();                                                     // SCL 时钟线拉低 准备接收数据位
        imu660ra_simiic_delay();
        IMU660RA_SCL_HIGH();                                                    // SCL 时钟线拉高 使数据线上数据有效
        imu660ra_simiic_delay();
        c <<= 1;
        if(GET_IMU660RA_SDA)                                                    // 读取 SDA 数据
        {
            c += 1;                                                             // 数据写入
        }
    }

    IMU660RA_SCL_LOW();                                                         // 拉低 SCL 进入应答部分
    imu660ra_simiic_delay();
    imu660ra_simiic_sendack(ack_x);                                             // 判断是否应答

    return c;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 向寄存器写入数据
// 参数说明     dev_add         设备地址(低七位地址)
// 参数说明     reg             寄存器地址
// 参数说明     dat             写入的数据
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_write_reg (uint8 dev_add, uint8 reg, uint8 dat)
{
    imu660ra_simiic_start();                                                    // 起始信号 发起通信
    imu660ra_send_ch( (dev_add << 1) | 0x00);                                   // 发送器件地址加写位
    imu660ra_send_ch( reg );                                                    // 发送从机寄存器地址
    imu660ra_send_ch( dat );                                                    // 发送需要写入的数据
    imu660ra_simiic_stop();                                                     // 结束信号
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 向寄存器写入多字节数据
// 参数说明     dev_add         设备地址(低七位地址)
// 参数说明     reg             寄存器地址
// 参数说明     *dat            数据保存的地址指针
// 参数说明     len             写入字节数量
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_write_regs (uint8 dev_add, uint8 reg, const uint8 *dat, uint32 len)
{
    imu660ra_simiic_start();                                                    // 起始信号 发起通信

    imu660ra_send_ch( (dev_add << 1) | 0x00);                                   // 发送器件地址加写位
    imu660ra_send_ch( reg );                                                    // 发送从机寄存器地址

    while(len --)
    {
        imu660ra_send_ch( *dat ++ );                                            // 发送需要写入的数据
    }

    imu660ra_simiic_stop();                                                     // 结束信号
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 从寄存器读取数据
// 参数说明     dev_add         设备地址(低七位地址)
// 参数说明     reg             寄存器地址
// 返回参数     uint8           返回寄存器的数据
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static uint8 imu660ra_simiic_read_reg (uint8 dev_add, uint8 reg)
{
    uint8 dat;

    imu660ra_simiic_start();                                                    // 起始信号 发起通信

    imu660ra_send_ch( (dev_add << 1) | 0x00);                                   // 发送器件地址加写位
    imu660ra_send_ch( reg );                                                    // 发送从机寄存器地址

    imu660ra_simiic_start();
    imu660ra_send_ch( (dev_add << 1) | 0x01);                                   // 发送器件地址加读位
    dat = imu660ra_read_ch(no_ack);                                             // 读取数据

    imu660ra_simiic_stop();                                                     // 结束信号

    return dat;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     模拟 IIC 从寄存器读取多字节数据
// 参数说明     dev_add         设备地址(低七位地址)
// 参数说明     reg             寄存器地址
// 参数说明     *dat_add        数据保存的地址指针
// 参数说明     num             读取字节数量
// 返回参数     void            无返回值
// 使用示例     
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static void imu660ra_simiic_read_regs (uint8 dev_add, uint8 reg, uint8 *dat_add, uint32 num)
{
    imu660ra_simiic_start();                                                    // 起始信号 发起通信

    imu660ra_send_ch( (dev_add << 1) | 0x00);                                   // 发送器件地址加写位
    imu660ra_send_ch( reg );                                                    // 发送从机寄存器地址

    imu660ra_simiic_start();

    imu660ra_send_ch( (dev_add << 1) | 0x01);                                   //发送器件地址加读位
    while(-- num)
    {
        *dat_add = imu660ra_read_ch(ack);                                       // 读取数据
        dat_add ++;
    }
    *dat_add = imu660ra_read_ch(no_ack);                                        // 读取数据

    imu660ra_simiic_stop();                                                     // 结束信号
}

#define imu660ra_write_register(reg, dat)        (imu660ra_simiic_write_reg(IMU660RA_DEV_ADDR, (reg), (dat)))
#define imu660ra_write_registers(reg, dat, len)  (imu660ra_simiic_write_regs(IMU660RA_DEV_ADDR, (reg), (dat), (len)))
#define imu660ra_read_register(reg)              (imu660ra_simiic_read_reg(IMU660RA_DEV_ADDR, (reg)))
#define imu660ra_read_registers(reg, dat, len)   (imu660ra_simiic_read_regs(IMU660RA_DEV_ADDR, (reg), (dat), (len)))


//-------------------------------------------------------------------------------------------------------------------
// 函数简介     IMU660RA 自检
// 参数说明     void
// 返回参数     uint8           1-自检失败 0-自检成功
// 使用示例     imu660ra_self_check();
// 备注信息     内部调用
//-------------------------------------------------------------------------------------------------------------------
static uint8 imu660ra_self_check (void)
{
    uint8 dat = 0, return_state = 0;
    uint16 timeout_count = 0;
    do
    {
        if(timeout_count ++ > IMU660RA_TIMEOUT_COUNT)
        {
            return_state =  1;
            break;
        }
        dat = imu660ra_read_register(IMU660RA_CHIP_ID);                         // 读取设备 ID
        osal_delay_millisec(1);
    }while(0x24 != dat);                                                        // 判断 ID 是否等于 0x24
    return return_state;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取 IMU660RA 加速度计数据
// 参数说明     void
// 返回参数     void
// 使用示例     imu660ra_get_acc();                                             // 执行该函数后，直接查看对应的变量即可
// 备注信息     
//-------------------------------------------------------------------------------------------------------------------
void imu660ra_get_acc (void)
{
    uint8 dat[6];
    imu660ra_read_registers(IMU660RA_ACC_ADDRESS, dat, 6);
    imu660ra_acc_x = (int16)(((uint16)dat[1] << 8 | dat[0]));
    imu660ra_acc_y = (int16)(((uint16)dat[3] << 8 | dat[2]));
    imu660ra_acc_z = (int16)(((uint16)dat[5] << 8 | dat[4]));
}
